import request from '@/utils/request'
const url = '/admin/admin.Message/'
// ----------------消息管理----------------
/**
 * 消息列表
 * @param {array} params 请求参数
 */
export function list(params) {
  return request({
    url: url + 'list',
    method: 'get',
    params: params
  })
}
